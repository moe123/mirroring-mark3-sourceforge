#include "led.h"

//---------------------------------------------------------------------------
void LED::Init(volatile uint8_t* pu8Port_, volatile uint8_t* pu8Dir_, uint8_t u8Bit_)
{
    m_pu8Port = pu8Port_;
    m_u8Pin = u8Bit_;

    *pu8Dir_ |= (1 << u8Bit_);
}

//---------------------------------------------------------------------------
void LED::Set()
{
    *m_pu8Port |= (1 << m_u8Pin);
}

//---------------------------------------------------------------------------
void LED::Clear()
{
    *m_pu8Port &= ~(1 << m_u8Pin);
}

//---------------------------------------------------------------------------
void LED::Toggle()
{
    *m_pu8Port ^= (1 << m_u8Pin);
}
